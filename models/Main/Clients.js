const mongoose = require("mongoose")

const ClientsSchema = new mongoose.Schema({
    name: {
        type: String
    },
    image: {
        type: String
    },
    sortOrder: {
        type: Number
    },
    addByName: {
        type: String,
        default:""
    },
    addByRole: {
        type: String,
        default:""
    },
    addById: {
        type: String,
        default:""
    },
    createDate: {
        type: String,
        default:""
    },
    updateByName: {
        type: String,
        default:""
    },
    updateByRole: {
        type: String,
        default:""
    },
    updateById: {
        type: String,
        default:""
    },
    updateDate: {
        type: String,
        default:""
    },
    status: {
        type: String,
        default: "Active"
    },
    remark: {
        type: String,
        default: ""
    }
})
const Clients = new mongoose.model("Clients", ClientsSchema)
module.exports = Clients